package general.operators.math;

public class MathExample {
    public static void main(String[] args) {
        System.out.println(Math.max(5, 6)); // 6
        System.out.println(Math.min(5, 6)); // 5
        System.out.println(Math.sqrt(2));  // racine de 2 =  1.4142135623730951
        System.out.println(Math.abs(-5));  // absolut of -5 = 5
        System.out.println(Math.PI);       // 3.141592653589793
        System.out.println(Math.log(5));   // 1.6094379124341003
        System.out.println(Math.random());
        System.out.println(Math.pow(2, 3)); // 8.0
    }
}
