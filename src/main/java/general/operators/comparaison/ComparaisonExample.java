package general.operators.comparaison;

public class ComparaisonExample {
    public static void main(String[] args) {
        int amirAge = 23;
        int antoineAge = 39;
        System.out.println(amirAge > antoineAge); //false
        System.out.println(amirAge < antoineAge); //true
        System.out.println(amirAge == antoineAge);//false
        System.out.println(amirAge != antoineAge);//true
    }
}
